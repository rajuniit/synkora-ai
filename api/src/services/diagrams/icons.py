def get_icon_svg(name: str, x: float, y: float, size: float = 24) -> str | None:
    """Render an icon as a positioned SVG element.

    Returns an ``<svg>`` fragment that can be embedded inside a parent SVG,
    positioned at *(x, y)* with the given *size*.  Returns ``None`` when the
    requested icon name is not in the registry.
    """
    icon = ICONS.get(name)
    if icon is None:
        return None

    vb = icon["viewBox"]
    paths = "".join(f'<path d="{p["d"]}" fill="{p["fill"]}"/>' for p in icon["paths"])

    return (
        f"<svg x={quoteattr(str(x))} y={quoteattr(str(y))}"
        f" width={quoteattr(str(size))} height={quoteattr(str(size))}"
        f" viewBox={quoteattr(vb)}>{paths}</svg>"
    )
